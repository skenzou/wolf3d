#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	int i;
	int s1_end;

	i = 0;
	s1_end = ft_strlen(s1);
	while (s2[i])
		s1[s1_end++] = s2[i++];
	s1[s1_end] = '\0';
	return (s1);
}
